	<h2>The art of online construction estimating</h2>
<p>&nbsp;</p>
<h2><a>1.1 Introduction</a></h2>
<p>Cost is important to all industry. Costs can be divided into two general classes; absolute costs and relative costs. Absolute cost measures the loss in value of assets. Relative cost involves a comparison between the chosen course of action and the course of action that was rejected. This cost of the alternative action - the action not taken - is often called the "opportunity cost".</p>
<p>The accountant is primarily concerned with the absolute cost. However, the forest engineer, the planner, the manager needs to be concerned with the alternative cost - the cost of the lost opportunity. Management has to be able to make comparisons between the policy that should be chosen and the policy that should be rejected. Such comparisons require the ability to predict costs, rather than merely record costs.</p>
<p>Cost data are, of course, essential to the technique of cost prediction. However, the form in which much cost data are recorded limits accurate cost prediction to the field of comparable situations only. This limitation of accurate cost prediction may not be serious in industries where the production environment changes little from month to month or year to year. In harvesting, however, identical production situations are the exception rather than the rule. Unless the cost data are broken down and recorded as unit costs, and correlated with the factors that control their values, they are of little use in deciding between alternative procedures. Here, the approach to the problem of useful cost data is that of identification, isolation, and control of the factors affecting cost.</p>
<p>&nbsp;</p>
<h2><a>1.2 Basic Classification of Costs</a></h2>
<p>Costs are divided into two types: variable costs, and fixed costs. Variable costs vary per unit of production. For example, they may be the cost per cubic meter of wood yarded, per cubic meter of dirt excavated, etc. Fixed costs, on the other hand, are incurred only once and as additional units of production are produced, the unit costs fall. Examples of fixed costs would be equipment move-in costs and road access costs.</p>
<p>&nbsp;</p>
<h2><a>1.3 Total Cost and Unit-Cost Formulas</a></h2>
<p>As harvesting operations become more complicated and involve both fixed and variable costs, there usually is more than one way to accomplish a given task. It may be possible to change the quantity of one or both types of cost, and thus to arrive at a minimum total cost. Mathematically, the relationship existing between volume of production and costs can be expressed by the following equations:</p>
<p>Total cost = fixed cost + variable cost &times; output</p>
<p><sub><img src="images/Articles/Introduction_to_Costing/t0579e00.gif" alt="t0579e00" width="249" height="45" /></sub></p>
<p>In symbols using the first letters of the cost elements and N for the output or number of units of production, these simple formulas are</p>
<p>C = F + NV</p>
<p>UC = F/N + V</p>
<p>&nbsp;</p>
<h2><a>1.4 Breakeven Analysis</a></h2>
<p>A breakeven analysis determines the point at which one method becomes superior to another method of accomplishing some task or objective. Breakeven analysis is a common and important part of cost control.</p>
<p>One illustration of a breakeven analysis would be to compare two methods of road construction for a road that involves a limited amount of cut-and-fill earthwork. It would be possible to do the earthwork by hand or by bulldozer. If the manual method were adopted, the fixed costs would be low or non-existent. Payment would be done on a daily basis and would call for direct supervision by a foreman. The cost would be calculated by estimating the time required and multiplying this time by the average wages of the men employed. The men could also be paid on a piece-work basis. Alternatively, this work could be done by a bulldozer which would have to be moved in from another site. Let us assume that the cost of the hand labor would be $0.60 per cubic meter and the bulldozer would cost $0.40 per cubic meter and would require $100 to move in from another site. The move-in cost for the bulldozer is a fixed cost, and is independent of the quantity of the earthwork handled. If the bulldozer is used, no economy will result unless the amount of earthwork is sufficient to carry the fixed cost plus the direct cost of the bulldozer operation.</p>
<p>Figure 1.1 Breakeven Example for Excavation.</p>
<p><img src="images/Articles/Introduction_to_Costing/t0579e0w.gif" alt="t0579e0w" width="594" height="385" /></p>
<p>If, on a set of coordinates, cost in dollars is plotted on the vertical axis and units of production on the horizontal axis, we can indicate fixed cost for any process by a horizontal line parallel to the x-axis. If variable cost per unit output is constant, then the total cost for any number of units of production will be the sum of the fixed cost and the variable cost multiplied by the number of units of production, or F + NV. If the cost data for two processes or methods, one of which has a higher variable cost, but lower fixed cost than the other are plotted on the same graph, the total cost lines will intersect at some point. At this point the levels of production and total cost are the same. This point is known as the "breakeven" point, since at this level one method is as economical as the other. Referring to Figure 1.1 the breakeven point at which quantity the bulldozer alternative and the manual labor alternative become equal is at 500 cubic meters. We could have found this same result algebraically by writing F + NV = F' + NV' where F and V are the fixed and variable costs for the manual method, and F' and V' are the corresponding values for the bulldozer method. Since all values are known except N, we can solve for N using the formula N = (F' - F) / (V - V')</p>
<p><sub><img src="images/Articles/Introduction_to_Costing/t0579e01.gif" alt="t0579e01" width="129" height="42" /></sub></p>
