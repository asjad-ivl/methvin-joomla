<h2><a>1.5 Minimum Cost Analyses</a></h2>
<p>A similar, but different problem is the determination of the point of minimum total cost. Instead of balancing two methods with different fixed and variable costs, the aim is to bring the sum of two costs to a minimum. We will assume a clearing crew of 20 men is clearing road right-of-way and the following facts are available:</p>
<blockquote>1. Men are paid at the rate of $0.40 per hour.<br />2. Time is measured from the time of leaving camp to the time of return.<br />3. Total walking time per man is increasing at the rate of 15 minutes per day.<br />4. The cost to move the camp is $50.</blockquote>
<p>If the camp is moved each day, no time is lost walking, but the camp cost is $50 per day. If the camp is not moved, on the second day 15 crew-minutes are lost or $2.00. On the third day, the total walking time has increased 30 minutes, the fourth day, 45 minutes, and so on. How often should the camp be moved assuming all other things are equal? We could derive an algebraic expression using the sum of an arithmetic series if we wanted to solve this problem a number of times, but for demonstration purposes we can simply calculate the average total camp cost. The average total camp cost is the sum of the average daily cost of walking time plus the average daily cost of moving camp. If we moved camp each day, then average daily cost of walking time would be zero and the cost of moving camp would be $50.00. If we moved the camp every other day, the cost of walking time is $2.00 lost the second day, or an average of $1.00 per day. The average daily cost of moving camp is $50 divided by 2 or $25.00. The average total camp cost is then $26.00. If we continued this process for various numbers of days the camp remains in location, we would obtain the results in Table 1.1.</p>
