<p>We see the average daily cost of walking time increasing linearly and the average cost of moving camp decreasing as the number of days the camp remains in one location increases. The minimum cost is obtained for leaving the camp in location 7 days (Figure 1.2). This minimum cost point should only be used as a guideline as all other things are rarely equal. An important output of the analysis is the sensitivity of the total cost to deviations from the minimum cost point. In this example, the total cost changes slowly between 5 and 10 days. Often, other considerations which may be difficult to quantify will affect the decision. In Section 2, we discuss balancing road costs against skidding costs. Sometimes roads are spaced more closely together than that indicated by the point of minimum total cost if excess road construction capacity is available. In this case the goal may be to reduce the risk of disrupting skidding production because of poor weather or equipment availability. Alternatively, we may choose to space roads farther apart to reduce environmental impacts. Due to the usually flat nature of the total cost curve, the increase in total cost is often small over a wide range of road spacings.</p>
