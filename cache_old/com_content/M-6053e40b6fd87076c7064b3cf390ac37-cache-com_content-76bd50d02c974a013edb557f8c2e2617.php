<h4><strong>Principles Of Construction</strong>&nbsp;</h4>
<p>This type of "pile wall" is typical employed in areas where headroom or proximity to buildings restricts the use of large diameter bored pile drilling rigs, or where the wall is to be located very close to an existing structure.</p>
<p>These are very time consuming and in allot of areas no-longer allowed as an acceptable safe construction method.</p>
<p>Stringent air monitoring and safety isses are adhered to during the construction of the caisson.&nbsp; An electric chain block and hoist structure is maintained at the pile head for access and egress of both materials and operatives.</p>
<p>&nbsp;</p>
<h4><strong>Site Investigation / Advanced works</strong></h4>
<p>Advanced grouting is often carried out around the group of caissons in order to reduce water inflow into the caisson area and provide adequate safety factor against base heave failer.&nbsp; Grouting of the area will also restrict ground settlement due to changes in the water table during caisson construction.</p>
<p>A 75 mm diameter grout hole is drilled to 2 m belowthe specified toe of the caisson.&nbsp; On completion of drilling, a 40 mm inside diameter PVC, Tube-a-Manchette (T-A-M) grout tube will be installed to the bottom of the grout hole.&nbsp;</p>
<p>The tube is usually perforated at 300 mm intervals with each perforation being covered by a tightly fitting rubber sleeve to act as one way valves.&nbsp; The grout hole is often backfilled with gravel and then pumped with grout to the specified pressures etc..&nbsp; After each stage of grouting the T-A-M will be flushed with water to allow future grouting as necessary.</p>
<p>The slightly conical excavation core is carried out by hand and a formwork erected and concrete placed in the void between the excavation and the internal formwork.&nbsp; Excavation down to the next level then begins.</p>
<p>&nbsp;</p>
<h4><strong>Excavation Of Caissons</strong></h4>
<p>All excavation is carried out with air tools and shovels etc.&nbsp; Material is loaded into a 0.2 m3 drum skip.&nbsp; Hand excavation to a depth not exceeding 1 metre.&nbsp;</p>
<p>Fix a suitably braced 3 segmented tapered shutter, fix reinformcement and pour concrete to fill all voids between the external face of the lining and the earth face.&nbsp; This operation is undertaken continously until the founding level is reached.</p>
<p>&nbsp;</p>
<h4><strong>Steel Reinforcement</strong></h4>
<p>Reinformcement is placed either in prefabricated cages or directly erected in side the bores, depending on headroom and caisson access conditions.</p>
<p>In some instances a steel "H" beam is installed in place of a reinforcement cage.</p>
<p>&nbsp;</p>
<h4><strong>Concreting</strong></h4>
<p>Concrete is cast in place in one continuous operation using the tremmie pipe method, up to cutt-off level.&nbsp; The concrete is generally not vibrated.</p>
<p>&nbsp;</p>
<h4><strong>As-Built Examples</strong></h4>
<ul>
<li>Perimeter wall caissons, 800 mm diameter, excavated to a depth of 26 metres (HK Central Subway).</li>
<li>Excavation Time&nbsp;18 - 24 days</li>
<li>Excavation to Concreteing Time&nbsp; 26 - 35 days</li>
<li>Difficult ground of sea wall or ground obstructions can easily increase theoverall construction time to 50 or 150 days.</li>
</ul>



