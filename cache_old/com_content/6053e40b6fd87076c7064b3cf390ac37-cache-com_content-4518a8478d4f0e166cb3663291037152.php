		<p>Project management is the discipline of defining and achieving targets while optimizing (or just allocating) the use of resources (time, money, people, materials, energy, space, etc) over the course of a project (a set of activities of finite duration).</p>
 
<p>Project Management is quite often the province and responsibility of an individual project manager. This individual seldom participates directly in the activities that produce the end result, but rather strives to maintain the progress and productive mutual interaction of various parties in such a way that overall risk of failure is reduced.</p>
<p>In contrast to on-going, functional work, a project is "a temporary endeavor undertaken to create a unique product or service." The duration of a project is the time from its start to its completion, which can take days, weeks, months or even years. Typical projects include the engineering and construction of various public or consumer products, including buildings, vehicles, electronic devices, and computer software.</p>
<p>In recent years, the Project Management discipline has been applied to Marketing and Advertising endeavors as they become more technologically oriented, and as multiple communication channels become part of the marketing mix.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<div>&nbsp;</div>
<p><a id="Project_Management_activities" name="Project_Management_activities"></a></p>
<h2>Project Management activities</h2>
<p>Project Management is composed of several different types of activities such as:</p>
<ol>
<li>Planning the work</li>
<li>Assessing risk</li>
<li>Estimating resources</li>
<li>Organizing the work</li>
<li>Acquiring human and material resources</li>
<li>Assigning tasks</li>
<li>Directing activities</li>
<li>Controlling project execution</li>
<li>Reporting progress</li>
<li>Analyzing the results based on the facts achieved</li>
</ol>
<div>&nbsp;</div>
<p><a id="Project_control_variables" name="Project_control_variables"></a></p>
<h2>Project control variables</h2>
<p>Project Management tries to gain control over five variables:</p>
<ul>
<li>time - The amount of time required to complete the project. Typically broken down for analytical purposes into the time required to complete the components of the project, which is then further broken down into the time required to complete each task contributing to the completion of each component.</li>
<li>cost - Calculated from the time variable. Cost to develop an internal project is time multiplied by the cost of the team members involved. When hiring an independent consultant for a project, cost will typically be determined by the consultant or firm's hourly rate multiplied by an estimated time to complete.</li>
<li>quality - The amount of time put into individual tasks determines the overall quality of the project. Some tasks may require a given amount of time to complete adequately, but given more time could be completed exceptionally. Over the course of a large project, quality can have a significant impact on time and cost (or vice versa).</li>
<li>scope - Requirements specified for the end result. The overall definition of what the project is supposed to accomplish, and a specific description of what the end result should be or accomplish.</li>
<li>risk - Potential points of failure. Most risks or potential failures can be overcome or resolved, given enough time and resources. According to some definitions risk can also be negative, meaning that there is an opportunity to e.g. complete the project faster than expected.</li>
</ul>
<p>Three of these variables can be given by external or internal customers. The value(s) of the remaining variable(s) is/are then set by project management, ideally based on solid estimation techniques. The final values have to be agreed upon in a negotiation process between project management and the customer. Usually, the values in terms of time, cost, quality and scope are contracted.</p>
<div>&nbsp;</div>
<p><a id="History_of_Project_Management" name="History_of_Project_Management"></a></p>
<h2>History of Project Management</h2>
<p>Project Management was used as an isolated concept before the Sputnik crisis of the Cold War. After this crisis, the United States Department of Defense needed to speed up the military project process. New tools (models) for achieving this goal were invented. In 1958 they invented the Program Evaluation and Review Technique or PERT, as part of the Polaris missile submarine program. At the same time, the DuPont corporation invented a similar model called CPM, critical path method. PERT was later extended with a work breakdown structure or WBS. The process flow and structure of the military undertakings quickly spread into many private enterprises.</p>
<div>&nbsp;</div>
<p><a id="Approaches" name="Approaches"></a></p>
<h2>Approaches</h2>
<p>There are several approaches that can be taken to managing project activities including agile, iterative, incremental, and phased approaches.</p>
<p>A traditional phased approach identifies a sequence of steps to be completed. This contrasts with the agile software development or flexible product development approach at the other end of the spectrum, in which the project is seen as a series of relatively small tasks conceived and executed as the situation demands in an adaptive manner, rather than as a completely pre-planned process.</p>
<p>Regardless of the approach employed, careful consideration needs to be given to clarity surrounding project objectives, goals, and importantly, the roles and responsibilities of all participants and stakeholders.</p>
<div>&nbsp;</div>
<p><a id="The_traditional_approach" name="The_traditional_approach"></a></p>
<h2>The traditional approach</h2>
<p>In the traditional approach, we can distinguish 5 components of a project (4 stages plus control) in the development of a project:</p>
<ol>
<li>project initiation</li>
<li>project planning</li>
<li>project production or execution</li>
<li>project monitoring or controlling</li>
<li>project completion</li>
</ol>
<p>Not all projects will visit every stage as projects can be terminated before they reach completion. Some projects probably don't have the planning and/or the monitoring. Some projects will go through steps 2, 3 and 4 multiple times.</p>
<p>Many industries utilize variations on these stages. For example, in bricks and mortar architectural design, projects typically progress through stages like Pre-Planning, Conceptual Design, Schematic Design, Design Development, Construction Drawings (or Contract Documents), and Construction Administration. While the names may differ from industry to industry, the actual stages typically follow common steps to problem solving--defining the problem, weighing options, choosing a path, implementation and evaluation.</p>
<p>Critical chain is an extension to the traditional critical path method.</p>
<p>In critical studies of project management, it has been noted that several of these fundamentally PERT-based models are not well suited for the multi-project company environment of today. Most of them are aimed at very large-scale, one-time, non-routine projects, and nowadays all kinds of management are expressed in terms of projects. Using complex models for "projects" (or rather "tasks") spanning a few weeks has been proven to cause unnecessary costs and low maneuverability in several cases. Instead, project management experts try to identify different "lightweight" models, such as, for example Extreme Programming for software development and Scrum techniques. The generalization of Extreme Programming to other kinds of projects is extreme project management, which may be used in combination with the process modeling and management principles of human interaction management.</p>
<div>&nbsp;</div>
<p><a id="Process-based_management" name="Process-based_management"></a></p>
<h2>Process-based management</h2>
<p>Also furthering the concept of project control is the incorporation of process-based management. This area has been driven by the use of Maturity models such as the CMMI (Capability Maturity Model Integration) and ISO/IEC15504 (SPICE - Software Process Improvement and Capability dEtermination), which have been far more successful.</p>
<p>Agile project management approaches based on the principles of human interaction management are founded on a process view of human collaboration.</p>
<div>&nbsp;</div>
<p><a id="Auditing" name="Auditing"></a></p>
<h2>Auditing</h2>
<p>Auditors may be involved in projects to varying degrees, from a post-implementation review to thorough involvement of each step in the process. Each project should be assessed for risk to determine the appropriate level of review needed. In addition, auditors should consider how important the projects are to the financial statements, the degree of reliance on controls, and the existence of manual controls.</p>
<p>Process risks include:</p>
<ul>
<li>Lack of a formal development process</li>
<li>Unclear strategy</li>
<li>Lack of concrete standards</li>
<li>Poor management control</li>
</ul>
<p>Application risks include:</p>
<ul>
<li>High complexity of the project</li>
<li>Larger projects</li>
<li>Lack of end-user involvement</li>
<li>Inadequate personnel</li>
</ul>
<div>&nbsp;</div>
<p><a id="Auditor_Review_and_Recommendations" name="Auditor_Review_and_Recommendations"></a></p>
<h3>Auditor Review and Recommendations</h3>
<p>Auditors should review the development process and procedures, even if they are not involved in a particular project. This review should evaluate the procedures and how they are implemented. The process of development and the quality of the final product may also be assessed if needed or requested. A business may want the auditing firm to be involved throughout the process to catch problems earlier on so that they can be fixed more easily. An auditor can serve as a controls consultant as part of the development team or as an independent auditor as part of an audit.</p>
<p>In making recommendations, auditors should consider the cost of implementing controls and alternatives such as manual controls. Recommendations should be forwarded to the development team leader, management, or the audit committee depending on the business. Clarifying the cost to the business if the control is not implemented in terms of errors, fixes, and additional audit fees.</p>
<div>&nbsp;</div>
<p><a id="Auditing_Formal_Software_Development_Processes" name="Auditing_Formal_Software_Development_Processes"></a></p>
<h3>Auditing Formal Software Development Processes</h3>
<p>Businesses sometimes use formal systems development processes. These help assure that systems are developed successfully. A formal process is more effective in creating strong controls, and auditors should review this process to confirm that it is well designed and is followed in practice.</p>
<p>A good formal systems development plan outlines:</p>
<ul>
<li>An information systems strategy to align development with the organization&rsquo;s broader objectives</li>
<li>Standards for new systems</li>
<li>Project management policies for timing and budgeting</li>
<li>Procedures describing the process</li>
</ul>
<div>&nbsp;</div>
<p><a id="Auditing_The_System_Development_Process" name="Auditing_The_System_Development_Process"></a></p>
<h3>Auditing The System Development Process</h3>
<p>Regardless of the methodology used, the development process should have the same major steps: planning, development, implementation, and maintenance.</p>
<p>The planning phase determines the nature and scope of the development. If this stage is not performed well, it is unlikely that the project will be successful in meeting the business&rsquo;s needs. The auditor&rsquo;s key role in this phase is to understand the business environment and to make sure that all necessary controls are incorporated into the design. Any deficiencies should be reported and a recommendation should be made to fix them.</p>
<p>In this planning stage, auditors look for a cohesive plan that encompasses the following areas:</p>
<ul>
<li>Study analyzing the business needs in measurable goals</li>
<li>Review of the current system</li>
<li>Conceptual design of the operation of the new system</li>
<li>Equipment requirements</li>
<li>Financial analysis of the costs and benefits including a budget</li>
<li>Select programmers, users, and support personnel for the project</li>
<li>Project plan including tasks, deliverables, and schedule</li>
</ul>
<p>After the planning phase, the system is built and tested. Testing is generally performed by a combination of testers and end users. Testing can occur after the product is built or concurrently. Auditors should review the construction and testing procedures and results to ensure that the product will process data accurately, that errors are minimized, and that it meets specifications. Testing verifies these factors:</p>
<ul>
<li>The product satisfies the user and business requirements</li>
<li>Functions as it was designed</li>
<li>Works with hardware and other software</li>
<li>Is free of errors</li>
</ul>
<p>The implementation phase includes:</p>
<ul>
<li>Conversion</li>
<li>Documentation</li>
<li>Training</li>
</ul>
<p>In software systems, conversion is the transfer of data from an old system to a new system. This process is often difficult and should be tested carefully for errors. Documentation is prepared both for implementers and end users to facilitate their different needs in understanding the system. Training increases user efficiency. From an auditor's perspective, training is also important because it helps users use the software correctly.</p>
<p><a id="Maintenance" name="Maintenance"></a></p>
<h3>Maintenance</h3>
<p>In software products, maintenance is an ongoing process, and it includes:</p>
<ul>
<li>Continuing support of end users</li>
<li>Correction of errors</li>
<li>Updates of the software over time</li>
</ul>
<p>In this stage, auditors should pay attention to how effectively and quickly user problems are resolved.</p>
<div>&nbsp;</div>
<p><a id="Project_management_and_professional_certification" name="Project_management_and_professional_certification"></a></p>
<h2>Project management and professional certification</h2>
<p>There have been several attempts to develop project management standards, such as:</p>
<ul>
<li>ISO 10006:1997, Quality management - Guidelines to quality in project management</li>
<li>A Guide to the Project Management Body of Knowledge (PMBOK Guide)</li>
<li>APM Body of Knowledge 5th ed. (APM - Association for Project Management (UK))</li>
<li>P2M (A guidebook of Project &amp; Program Management for Enterprise Innovation, japanese third-generation project management method)</li>
<li>PRINCE2 (PRojects IN a Controlled Environment)</li>
<li>V-Modell (German project management method)</li>
<li>HERMES (The Swiss general project management method, selected for use in Luxembourg and international organisations)</li>
<li>ISEB Project Management Syllabus</li>
</ul>
<p>See also: An exhaustive list of standards (maturity models)</p>
<p>So far, there is no known attempt to develop a project management standard available under the GNU Free Documentation License. There is a proposed Project Management XML Schema.</p>
<p>There is an effort by PMI to develop The Practice Standard for Scheduling. (This document is currently (May 2006) in exposure draft, which is near the end of the standards development process.)</p>
