		<p>The construction of a project has several stages of completion prior to the actual finishing point. A project undergoes mainly five stages to be achieved fully, and these are described below in detail:</p>
<h4>&nbsp;</h4>
 
<h4>Design</h4>
<p>The design stage is the preliminary stage of a construction project, and the bidding process comes to pass during this project stage. The design team is responsible for making certain that the design meets the regulations and codes of the building. This stage of a construction project includes four major sub-steps as defined below:</p>
<p>Programming &amp; feasibility: To start with the actual designing of the project, the desires, objectives and goals are ought to be decided for the project/building. The decisions, in this stage, are required to be made based on the number of rooms, size of the building, number and type of occupants, and depending on how the space would be employed.&nbsp;</p>
<p>Schematic design: These are sketches used to identify patterns, shapes and spaces, and these sketches must also take into consideration the textures, materials, colors and sizes as well.</p>
<p>Design development (DD): The third step undertakes thorough research into the type of equipments and materials that should be used in the project construction, along with their respective costs.</p>
<p>Contract documents (CDs): Also known as working drawings, these are the construction project&rsquo;s final specifications and drawings. Builders use these documents for the purpose of construction, while contractors use them to settle on their bid.&nbsp;</p>
<p>&nbsp;</p>
<h4>Pre-Construction</h4>
<p>This second stage of a construction project commences when a notice is given by the owner to move on to the contractor who has been chosen by the process of bidding. The proceeding notice signifies that the owner offers consent to the contractor for starting the construction work on the building. In this stage, the owner allots the project team that comprises of the contract administrator, project manager, field engineer and superintendent. A site examination is also essential during this stage of pre-construction to ascertain if any other steps are required to be put into practice on the job site. Prior to the actual process of construction, the site gets all set in this stage, and it, in addition, takes account of any unanticipated situations like environment problems or historical artefacts. Moreover, this stage also calls for a soil test to verify if the condition of soil is good or not.&nbsp;</p>
<p>&nbsp;</p>
<h4>Procurement</h4>
<p>The third stage of procurement is when the required equipment, materials, and labor are purchased for the project completion. A general contractor can undertake this task in cases where the entire work of construction is done by the company itself. There is also a possibility that a sub-contractor might be appointed by the contractor to assume this task of procurement. Contractors who concentrate in a specific aspect of the construction work like carpentry, glass, melding, or concrete are known as Subcontractors.</p>
<p>&nbsp;</p>
<h4>Construction</h4>
<p>The superintendent brings in concert a pre-construction meeting during this stage of a construction project. This meeting takes into consideration and discusses issues such as site access, quality control, material storage, and work hours. The subsequent step is to shift everything on the construction locate and take hold of it all.</p>
<p>&nbsp;</p>
<h4>Post-Construction</h4>
<p>On completion of the project, a few steps must be followed to set up the building for possession. These steps comprise of the signing of documentation and paperwork, and the release of final imbursement to the contractor.</p>	</div>

	
	
