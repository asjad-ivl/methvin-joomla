<h4 dir="ltr">Multi-currency Estimating</h4>
<p dir="ltr">A major difficulty facing businesses bidding on large International Projects is dealing with multiple currencies within the same estimate. When projects are funded with international funding in multiple currencies or when suppliers or subcontractors are providing material or services in a multitude of currencies. Funding Organizations often require as a prerequisite in providing funds that goods and services be provided in the same currency that the financing is provided. To estimate projects in multiple currencies, the estimator requires a system that allows resources to be saved in multiple currencies, as well as different currency values for each of those resources.</p>
<p dir="ltr">&nbsp;</p>
<p dir="ltr">Converting Resources between different Currencies</p>
<ul>
<li dir="ltr">
<p dir="ltr">To efficiently bid in multiple currencies your resource table must<br />allow the user to set the base rate in its base currency and then provide a<br />procedure to convert the value into the projects currency.</p>
</li>
</ul>
<ul>
<li dir="ltr">
<p dir="ltr">For material and labour resources this is a very straight forward process,<br />which involves just multiplying the rates by the appropriate exchange rate.</p>
</li>
</ul>
<p dir="ltr">&nbsp;</p>
<p dir="ltr">Entering and Comparing Quotations in Different Currencies</p>
<ul>
<li dir="ltr">
<p dir="ltr">In a single estimate various suppliers may also quote on the same<br />commodity in different currencies. The system must save the quotation in the<br />source currency but also convert the currency to the base currency of the<br />estimate so the quotations can be compared against each other.</p>
</li>
</ul>
<p dir="ltr">&nbsp;</p>
<p dir="ltr">Generating Cash Flows in Multiple Currencies</p>
<ul>
<li dir="ltr">
<p dir="ltr">One of the prime purposes of creating an estimate in multiple<br />currencies is that the financing will be procured in these same currencies. It<br />is therefore important to have the ability to generate separate cash flows in<br />each currency.</p>
</li>
</ul>



