		<h4>WBS</h4>
<p>The work breakdown structure (WBS) module is the latest module incorporated into the Methvin estimating package. It allows for two independent work breakdown structures. The first allows you to present the project in the manner the client expects to see it. And the second provides an opportunity to break the project into a hierarchical and incremental decomposition of the project into phases, deliverables and work packages. It is a tree structure,&nbsp;which shows a subdivision of effort required to achieve an objective; for example a program, project, and contract. In a project or contract, the WBS is developed by starting with the end objective and successively subdividing it into manageable components in terms of size, duration, and responsibility (e.g., systems, subsystems, components, tasks, subtasks, and work packages) which include all steps necessary to achieve the objective.</p>
<div>
<p>The work breakdown structure provides a common framework for the natural development of the overall planning and control of a contract and is the basis for dividing work into definable increments from which the statement of work can be developed and technical, schedule, cost, and labour hour reporting can be established.</p>
<p>A work breakdown structure (WBS) permits summing of subordinate costs for tasks, materials, etc., into their successively higher level &ldquo;parent&rdquo; tasks, materials, etc. For each element of the work breakdown structure, a description of the task to be performed is generated. This technique (sometimes called a system breakdown structure) is used to define and organize the total scope of a project.</p>
<p>&nbsp;</p>
<h4>History of work breakdown structure</h4>
The concept of work breakdown structure developed with the Program Evaluation and Review Technique (PERT) by the United States Department of Defense (DoD). PERT was introduced by the U.S. Navy in 1957 to support the development of its Polaris missile program. While the term "work breakdown structure" was not used, this first implementation of PERT did organize the tasks into product-oriented categories.</div> 	</div>

	
	
