<p class="c26 c1 c15"><span>If the file you are working with is an online editable document then you get an additional field in the detail view: File contents lets you preview the content of the file without going to edit view.</span></p>
<h2 class="c21 c1 c15"><a name="h.kat8vutoxd33"></a><span class="c11">Document Management</span></h2>
<p class="c5 c1"><span>Like in any collaboration tool you can upload and download documents with Methvin. And like any content object every document has its properties which you can edit. Since you may be familiar with both concepts, managing documents is quite simple.</span></p>
<h3 class="c19 c1 c15"><a name="h.3h79pg5adaia"></a><span class="c11 c16">Uploading files</span></h3>
<p class="c5 c1"><span>To upload a new file choose “New &gt; Upload a file” in the toolbar.</span></p>
<p class="c5 c1"><span>When uploading a file you have the possibility to give it a new file name and to set all its properties. (You can edit all this at a later stage as well.) Please note the message indicating restrictions around file types and size for upload. (The maximum file is not set by Methvin itself but by some parameters in the php.ini.)</span></p>
<h3 class="c19 c1 c15"><a name="h.1pe0omk7f3q2"></a><span class="c11 c16">Uploading multiple files</span></h3>
<p class="c5 c1"><span>If you want to upload only a few files the procedure described above is optimal. &nbsp;If you are trying to upload a larger number of files this procedure is time consuming. As an alternative you can pack all files in one single ZIP archive, upload the ZIP archive in the Documents section, an unzip the archive on the server. Archives have a different icon (shown left) and a button to extract the files from the archive (shown right).</span></p>
<p class="c5 c1"><span>[Pic]</span></p>
<h3 class="c19 c1 c15"><a name="h.pnpdj7b8tcfp"></a><span class="c11 c16">Downloading files</span></h3>
<p class="c5 c1"><span>[Pic]</span></p>
<p class="c5 c1"><span>To download a file go to the detail view of this file, then choose “Download” in the “Actions” menu.</span></p>
<h3 class="c19 c1 c15"><a name="h.e0imhjgiz6cv"></a><span class="c11 c16">Downloading multiple files</span></h3>
<p class="c5 c1"><span>For efficiency, </span><span>pack all files into a ZIP archive by selecting the needed files and clicking the “Compress” button. Note this will be a HUGE file and you may want to consider deleting it from the server once it is downloaded as a ZIP file.</span></p>
