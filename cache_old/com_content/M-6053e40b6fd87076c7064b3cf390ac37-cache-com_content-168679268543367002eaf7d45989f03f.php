<h1 class="c1"><a name="h.8cs5qhkmvmfc"></a><span>Import &amp; Export options</span></h1>
<h2 class="c1"><a name="h.7fra7gmvokev"></a><span>Import Client schedule</span></h2>
<h3 class="c12 c1"><a name="h.g0gmyc5a3y8"></a><span>Importing a client supplied schedule</span></h3>
<p class="c8 c1"><span>The best practice to insure a trouble free import of the Clients Schedule is by following this step by step procedure:</span></p>
<ol class="c18" start="1">
<li class="c2 c1"><span>First export an existing schedule, by clicking the Export to Excel Icon in the Pricing Module.</span></li>
<li class="c2 c1"><span>This will give you the excel import structure (i.e. Column header descriptions and sequence of columns) for your import.</span></li>
<li class="c2 c1"><span>Copy your new client supplied information to their appropriate column headers, then save the file.</span></li>
<li class="c2 c1"><span>Click on the Import Schedule icon to upload the clients schedule, Navigate to your newly created file and select it, then click the save button, this will import your client supplied schedule into the pricing module. </span></li>
</ol>
<p class="c1"><span>(pic)</span></p>
<h2 class="c1"><a name="h.g2mn5qjp7q6t"></a><span>Export Projects</span></h2>
<h3 class="c12 c1"><a name="h.csku9yci3i12"></a><span>Important information to remember when exporting a project</span></h3>
<p class="c12 c1"><span>When exporting a Project file, it is important to remembered that only the physical text data is saved, none of the uploaded files in the documents module are backed up to the file. When backing up your documents it is recommended you download them to your local computer. There is a “save” process within the system to allow you to zip save the entire documents content to your local drive.</span></p>
<p class="c12 c1"><span>(pic)</span></p>
<h3 class="c12 c1"><a name="h.xg9gvvbe04wb"></a><span>Export a specific Project</span></h3>
<p class="c8 c1"><span>From the Projects window situated on the left hand side of the screen, right click on the Project you wish to download. This brings up the projects quick menu, select the Export Project menu item. This will save the project sql file to your computer. Important note, this does not save your uploaded files which were saved under the Documents Module. </span></p>
<h2 class="c1"><a name="h.d4jf35887cc"></a><span>Import Project</span></h2>
