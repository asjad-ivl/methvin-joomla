<h2 class="c1"><a name="h.3vziv3i4224x"></a><span>Imbalanced Rates</span></h2>
<p class="c12 c1"><span>Once you have accurately priced up an item and come up with its cost rate the item is automatically marked up by the default markup as set in the Pricing Module. If for any reason you believe the item to be over or under scheduled, it is very easy to adjust its markup by either inputting the required Gross Rate or inputting the required percentage markup. This then automatically locks in the new markup by setting the lock gross rate checkbox to “true”.</span></p>
<h2 class="c1"><a name="h.1gbtbus8d6iw"></a><span>Markup - % for the Entire Project</span></h2>
<p class="c12 c1"><span>In the top right hand side of the screen under the Pricing Module it is possible to select the percentage that you would like to mark up the entire project. Once you have selected the required markup, click on the “Calculate” button at the top of the screen, this will adjust the percentage markup for the entire project to your revised markup percentage. If any items have been locked to allow for imbalanced items rates, they will be ignored and their previous markup will remain.</span></p>
<h2 class="c1"><a name="h.2l3vlfut66cm"></a><span>Markup - Individual Items</span></h2>
<p class="c12 c1"><span>For example if you wish to markup a section of the schedule by 3% and the rest of the project by 8.5% it is possible to set the global percentage markup as described before to 3% then click the calculate button. Individually set the ‘Lock?’ button to true for the section of the schedule you wish to mark up by 3%. Change the global percentage markup to 8.5% and click the calculate button once more, this will then markup the remainder of the scheduled items which don't have there checkboxes selected up the required 8.5%.</span></p>
