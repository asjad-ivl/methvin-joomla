<h2 class="c21 c1 c15"><a name="h.4b29biuxbwt"></a><span>Direct URL - regular login screen</span></h2>
<p class="c8 c1"><span>The regular login screen is what you see when you write the direct URL for your Methvin installation.</span></p>
<p class="c8 c1"><span>[Login Pic]</span></p>
<p class="c8 c1"><span>You will be able to log into Methvin with either your email or your username. This way you can avoid having to remember yet-another-username.</span></p>
<h2 class="c21 c1 c15"><a name="h.4qicgeqbcbsx"></a><span>Recover a forgotten password</span></h2>
<p class="c8 c1"><span>If you forgot your password, click the “Forgot password?” link on the login screen. You will get a form that allows you to request a new password. Since e-mails are sent unencrypted you should change this temporary password immediately after the first login in your </span><span class="c0"><a class="c3" href="#id.9bvfu3n7t6x7">user account</a></span><span>.</span></p>
<p class="c8 c1"><span>[Forgot Username Pic]</span></p>
<p class="c8 c1"><span>PLEASE NOTE: You can only request a new password with an e-mail address that is already stored in an existing user account, and the new password will be sent to that address. If you get an error message you have probably been using Methvin with another e-mail address.</span></p>
<p class="c8 c1"><span>[Forgot Password Pic]</span></p>
<h2 class="c21 c1 c15"><a name="h.ee4ivuuflvmk"></a><span>Website-</span><span>Methvin</span><span>&nbsp;Single User login</span></h2>
<p class="c8 c1"><span>If your account is a Methvin Single User account, you will be automatically redirected to your Companys default pricing page.</span></p>
<p class="c8 c1"><span>If you have the Professional Addition, the site will provide you with the list of your available Companies.</span></p>
<p class="c8 c1"><span>If the system does not automatically log you in, or does not present you with the installation options, please contact Methvin Support Team, your Methvin account executive, or your organization Technical Support representative.</span></p>
