<h1 class="c1"><a name="h.clmqtvjv1d1p"></a><span>Documentation Module</span></h1>
<h2 class="c1 c15"><a name="h.1lhomxahujz6"></a><span>Documents</span></h2>
<p class="c1"><span>One of the key features of Methvin is that you can not only upload, download and share any type of document but that you can edit files online to a certain degree as well. We therefore distinguish between the document management features and the document editing features of Methvin.</span></p>
<h2 class="c1 c15 c21"><a name="h.bsmmfaqa05as"></a><span class="c11">List view</span></h2>
<p class="c8 c1"><span>In the list view of the Documents module you can see all files of the current project. Note the Edit button, and below Actions, some files have this and others don't. This distinguishes between files that are editable online and files that can just be downloaded.</span></p>
<p class="c8 c1 c15"><span>[Pic]</span></p>
<h3 class="c19 c1 c15"><a name="h.c6jlnzeoks3b"></a><span class="c11 c16">Actions</span></h3>
<p class="c5 c1"><span>When the Actions column in the list view is active you will notice that there are four different types of documents, with different options in this column:</span></p>
<p class="c5 c1"><span>[Pic]</span></p>
<p class="c5 c1"><span>The first action (the arrow button) is available for most document types; it lets you download the currently selected files to your local hard drive. </span></p>
<p class="c5 c1"><span>The rest is specific to a certain document type - Note the following examples.</span></p>
<ol class="c24" start="1">
<li class="c4 c1"><span>The standard document (for example a Word or a Photoshop file); this type of document can only be downloaded.</span></li>
<li class="c4 c1"><span>The editable document; here you have the choice between downloading and editing.</span></li>
<li class="c4 c1"><span>ZIP archive; you can unzip it directly on the server, or you can download it as you would any other document.</span></li>
<li class="c4 c1"><span>PDF document which can be viewed on the screen or downloaded.</span></li>
</ol>
<h3 class="c19 c1 c15"><a name="h.q9hvjbm10n0j"></a><span class="c11 c16">Toolbar </span></h3>
<p class="c1 c34"><span class="c14">[Pic]</span></p>
<ol class="c18" start="1">
<li class="c4 c1"><span>Upload a file: Lets you upload a file from your local hard drive to Methvin. This file can be of any type.</span></li>
<li class="c4 c1"><span>Document: Lets you create an online editable rich text document (which is stored as an HTML file).</span></li>
</ol><ol class="c18" start="1">
<li class="c4 c1"><span>Update file: Allows you to update the selected file.</span></li>
</ol><ol class="c18" start="1">
<li class="c4 c1"><span>Delete: This removes the file from the database</span></li>
</ol><ol class="c18" start="1">
<li class="c4 c1"><span>Mark as read: Marks the selected file(s) as read.</span></li>
<li class="c4 c1"><span>Mark as unread: Marks the selected file(s) as unread.</span></li>
</ol>
<h3 class="c19 c1 c15"><a name="h.y4hylyfztnts"></a><span class="c11 c16">Detail view</span></h3>
<p class="c1 c15 c26"><span class="c11 c14 c32 c16">[Pic]</span></p>
<h3 class="c19 c1 c15"><a name="h.r19plusv1rnj"></a><span class="c11 c16">Actions</span></h3>
<ol class="c18" start="1">
<li class="c1 c4"><span>Slideshow (only available if the file is an online editable presentation): Displays the file in presentation mode, opening a new window.</span></li>
<li class="c4 c1"><span>Download: Downloads the file to your local hard drive.</span></li>
<li class="c4 c1"><span>Edit this file (only available if the file is an online editable document or presentation): Takes you to the online editor.</span></li>
<li class="c4 c1"><span>Update file: Allows you to update the selected file. Once you get in there, you may find an option that would let you keep a copy of the old version of the file as well. (This button is only available if you select one single document.)</span></li>
<li class="c4 c1"><span>Delete: This removes the file from the database</span></li>
<li class="c4 c1"><span>Archive: Archives the selected file into the Archived Objects folder.</span></li>
<li class="c4 c1"><span>Copy this file: Creates an exact copy of this file.</span></li>
</ol>
<h3 class="c19 c1 c15"><a name="h.gnijr7hhzf8c"></a><span class="c11 c16">Properties</span></h3>
<ol class="c18" start="1">
<li class="c4 c1"><span>Project: Workspace where this document is allocated to. Clicking on a Project name to select it, takes you to the Documents relating to this Project.</span></li>
<li class="c4 c1"><span>Created by: User who created this document (including the creation date).</span></li>
<li class="c4 c1"><span>Modified by: User who last modified this document (including the date of the last modification).</span></li>
</ol>
