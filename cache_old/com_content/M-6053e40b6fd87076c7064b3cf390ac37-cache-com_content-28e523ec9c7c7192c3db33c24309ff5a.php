<p align="-webkit-auto">Principles of Construction Often known as "Colcrete" piling, which is a type of trade name. Usually replaced by diaphragm walling methods.</p>
<p align="-webkit-auto">Essentially typical bored piles, or down-the-hold bored piles, are constructed with a spacing such that each pile is virtually touching the next. These are constructed on a hit-and-miss basis.</p>
<p align="-webkit-auto">After a suitable length of these piles are in place (often 9 piles or so) a smaller drilling rig follows on behind placing the "colcrete" piles. A small diameter shaft, often 100 mm or so, is drilled on the external face of the pile wall close to the interection between the two main piles. This excavated pile is fitted with a grout tube and backfilled with gravel. The pile is then grouted and forms a seal between the adjacent piles. This type of pile wall can be constructed with either a standard reinforcement cage or a "H" pile.</p>
<p align="-webkit-auto">&nbsp;</p>
<p align="-webkit-auto"><strong>Info below achieved on project in Bermonsey, London - Sept 2010</strong></p>
<p align="-webkit-auto">1 rig, 600dia contig piles, 12-15m deep, 12No per day (average), 15No per day (max)</p>
<p align="-webkit-auto">Rule of thumb based on Hit 1, Miss 3&nbsp;= 8.5l/m day per rig.</p>
<p align="-webkit-auto">Labour: 1 Rig driver, 1 banksman, 1 conc pump operative, 3 x steel fixers, 1 x 360 driver</p>
<p align="-webkit-auto">Plant: 1x rig, 1x mini 360 exc, 1x 6m3 mixer, 1x pump, -&nbsp;</p>
<p align="-webkit-auto">Deliveries: 4 - 5No 6m3 wagons per rig per day (av 2m3 per pile)</p>
<p align="-webkit-auto">Mobilisation: 1 day (Allow for 2 days)</p>
<p align="-webkit-auto">Next trade -&nbsp;Excavate and blind&nbsp;around piles (for ring beam)&nbsp;- start&nbsp;7 elasped days after last piles&nbsp;in the run are complete (4 working and 7 elasped days after piling start&nbsp;based on Hit 1 miss 3 method and 12No/d).&nbsp;&nbsp;34 linear metres&nbsp;released&nbsp;for Groundworker to start.</p>
<p align="-webkit-auto">All piles 600mm in dia. No smaller piles or grout tubes&nbsp;used as described&nbsp;in the post by technical development. The face of the basement wall will be faced with a&nbsp;single skin of blockwork at a later date (non critical).</p>
<p align="-webkit-auto">Method: Hit 1, Miss 3</p>



