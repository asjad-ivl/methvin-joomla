<p>Risks are acknowledged and examined in an arbitrary, brainstorming manner in the majority of construction projects. This is frequently lethal to the project&rsquo;s success, as unforeseen risks come to pass. These risks, instead of being reckoned with and warded off in a premeditated and unhurried manner, have not actually been planned or assessed and is thus required to be taken into hand without delay basis. In the initial stages of planning and preparation in risk assessment and management, it is vital to identify, characterize and assess the prospective risks. Instead of considering each risk arbitrarily and independently, it is exceedingly effectual to spot out risks and then designate them into groups, and then to draft a category list and finally classify possible risks that may lie within each group/category. In this fashion, common influences, causes, factors, possible impacts and probable corrective and/or precautionary actions, could be considered and settled on.</p>



	
