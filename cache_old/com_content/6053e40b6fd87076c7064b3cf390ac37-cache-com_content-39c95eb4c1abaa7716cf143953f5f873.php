<h4><strong>Removable Ground Anchors</strong></h4>
<p><span face="tahoma, verdana, arial, helvetica, sans-serif" color="#333333">Removable Ground Anchors Recent innovation of "removable" ground anchor system. Steel cables are doubles up and threaded over a roller at the anchor end. Once de-stressed, they can be pulled out. 1 rig can achieve...</span></p>
<ul>
<li>Drill 30 m shaft 7.0 hrs</li>
<li>Install strands/setup anchor 6.0 hrs</li>
<li>Grout in strands 2.0 hrs</li>
<li>Stressing can be done 4 - 7 days after grouting.</li>
</ul>
<p>&nbsp;</p>
<ul></ul>
<p>Usually at 3 m horizontal centres and can only be worked on with 5 - 6 m spacings. So a hit and miss method must be adopted, giving a significant programme constraint.</p>



