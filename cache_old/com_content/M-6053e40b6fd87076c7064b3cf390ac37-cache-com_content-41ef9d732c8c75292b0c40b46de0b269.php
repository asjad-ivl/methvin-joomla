<h2><a>1.1 Introduction</a></h2>
<p>Cost is important to all industry. Costs can be divided into two general classes; absolute costs and relative costs. Absolute cost measures the loss in value of assets. Relative cost involves a comparison between the chosen course of action and the course of action that was rejected. This cost of the alternative action - the action not taken - is often called the "opportunity cost".</p>



	
