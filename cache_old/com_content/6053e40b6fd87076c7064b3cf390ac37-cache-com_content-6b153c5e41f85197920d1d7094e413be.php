<h4><strong>Multiple Bell Ground Anchors</strong></h4>
<p>Multiple Bell Ground Anchors Often used in London Clay.</p>
<ul>
<li>Uncased, 10 m long 2.0 no/day</li>
<li>Top Cased type, 15 m long 1.0 no/day</li>
<li>Anchor Stressing 2.0 no/day</li>
