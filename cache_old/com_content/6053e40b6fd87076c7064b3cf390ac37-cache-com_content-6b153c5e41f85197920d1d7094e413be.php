<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>The Target Founding level</strong></p>
<ul>
    <li>This is defined as the required socket into the bedrock, which is defined as moderately decomposed rock grade III or better with a core recovery greater than 85% (allowable bearing capacity of 5 mPa).&nbsp; The continuity of the founding rock is demonstrated by continuing the pre-bored hole a maximum of 5 metres or 3 times the pile diameter, whichever is the greater.</li>
    <li>The cores are logged, stored and photographed and submitted together with proposed founding levels for approval.</li>
</ul>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>Setting Out</strong></p>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;">&nbsp;</p>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>&nbsp;</strong>Before starting excavation at the pile position the following steps are taken:</p>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;">&nbsp;</p>
<ul>
    <li>Survey and record the existing ground level at the pile position</li>
    <li>Setout the pile location from the reference points and in order to monitor the position of the steel casing, control pins are usually established at two orthogonal positions, offset from the centre of the pile.</li>
</ul>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>Pile Tolerances</strong></p>
<ul>
    <li>In the case of out of position casings, adjustment can be made to keep the vertical alignment and plan position within the limits of no more than 75 mm off-centre on plan position and not deviating by more than 1:75 from the vertical axis.</li>
</ul>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>Pile Excavation / Casings</strong></p>
<ul>
    <li>The shaft of the pile is excavated within a temporary steel casing with an outside diameter of say approximately 200 - 300 mm greater than the pile diameter.&nbsp; The casing is used mainly in areas of unstable ground and are driven using hydraulic casing oscillator attached to a crawler crane or a casing vibrator.</li>
    <li>Shaft excavation is carried out using a single or double hammer grab supported by crawler crane.&nbsp; The steel casing toe is kept in front of the excavation level until it is 0.5 metres above the pile cut-of level.&nbsp; The pile shaft is often flooded with bentonite or water and excavation proceeds to the top of the CDG.</li>
    <li>Excavation then proceeds by reverse circulation drilling (RCD) using large diameter drilling heads with special rock cutters and flushing by air lift.&nbsp; Bentonite or water levels are always maintained above the ground water level to ensure stability of the shaft.</li>
</ul>
