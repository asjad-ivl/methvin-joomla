    <li>Pile concreting is carried out under water by "tremie" techniques maintaining the water or bentonite head inside the casing at or above existing ground water level.&nbsp; The tremie tube (250mm) is withdrawn as concreting proceeds ensuring a minimum concrete head of 2 metres above the top of the tremie tube.</li>
</ul>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>Sequence Of Piling</strong></p>
<ul>
    <li>The sequence of construction of piles is chosen in such a manner that no damage can be caused to nearby piles still under construction or recently concreted (i.e. less than 3 days).</li>
    <li>On a 12 metre grid, a normal layout would mean say having two non-worked on piles between each open excavation in the longitudinal direction (ie a 36 metre spacing) thus allowing room for the crane etc and a lesser spacing of every other pile being worked on (ie a 24 metre spacing).</li>
</ul>
<p style="margin: 10px 0px; padding: 0px; line-height: 19px; color: #333333; font-family: tahoma, verdana, arial, helvetica, sans-serif;"><strong>&nbsp;Pile Testing</strong></p>
<ul>
    <li>The workability of concrete is tested on site by measuring the slump and temperature of concreting at the time of discharge into the pile shaft.&nbsp; Laboratory tests are carried out in order to check the strength of the placed concrete.&nbsp; A number of test cubes are made and tested at 7 and 28 days.</li>
</ul>
<ul>
    <li><strong>Coring Test&nbsp;</strong>-&nbsp;Certain piles selected by the Engineer will be cored their full depth.&nbsp; The depth of cores into the base material (rock) will usually be at least 600 mm.&nbsp; Cores are placed in correct order and relative position in core boxes which clearly mark the depths of cores.&nbsp; The cores are usually photographed and submitted to the Engineer.&nbsp; The testing of the coring will provide additional information about the quality of the concrete as well as the condition of the interface between concrete and rock.</li>
    <li><strong>Sonic Logging Test</strong>&nbsp;-&nbsp;In order to test the quality of the concrete as well as the integrity of the pile in its overall length and pile toe condition sonic core testing is used.&nbsp; Sonic tubes are installed with the reinforcement cage in order to allow the lowering of the signal transmitter and signal receiver probe down the bottom of the pile.&nbsp; These tubes are sealed at the bottom.</li>
    <li><strong>Vibration Tests</strong>&nbsp;-&nbsp;This test determines the pile length and shape and the overall pile concrete quality.&nbsp; This is a specialist test.</li>
</ul>



