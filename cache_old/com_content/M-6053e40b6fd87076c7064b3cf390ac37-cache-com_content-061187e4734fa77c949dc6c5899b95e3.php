<h4><strong>Principles Of Construction</strong></h4>
<ul>
<li>Often known as "Benoto" piling, which is a type of trade name.&nbsp; Usually replaced by diaphragm walling methods.</li>
<li>Essentially typical bored piles, or down-the hold type bored piles, are constructed with a spacing slightly smaller than the chosen diameter of the piles.&nbsp; Once a suitable length of piling has been completed, the "holes" are filled with a similar bored pile which intersects and "bites" into the two previously constructed piles.</li>
<li>Usuall these piles are strengthened with a steel "H" pile, rather than the typical reinforcement cages used in standard bored piling.</li>
</ul>
<p>&nbsp;</p>
<ul></ul>
<h4><strong>As-Built Examples</strong></h4>
<ul>
<li>Large diameter circular secant pile cofferdam.</li>
<li>Structure consisting of 1,200 mm diameter secant piles to a depth of 20 which then stepped into 900 mm contiguous piles for a further 20 m depth.&nbsp; Each pile was probed to 10 m depth in advance.&nbsp; A total of 255 number piles were constructed.</li>
<li>The "cofferdam" had a 1.4 m deep concrete base slab with a concrete lining to the walls.</li>
<li>An intermediate concrete roof slab was placed to form an underground box structure.</li>
<li>Overall construction time&nbsp; 12 weeks (Heathrow Express).</li>
</ul>



