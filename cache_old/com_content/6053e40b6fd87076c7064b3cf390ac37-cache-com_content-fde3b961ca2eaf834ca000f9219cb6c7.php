<div>&nbsp;</div>
<div>&nbsp;</div>
<div>Hydroelectricity is not just about big dams and thousands of mega watts projects. There are small hydro electric projects which are considered sustainable and effective. In the rural regions where power transmission is very difficult, these plants are helpful. Energy is produced by installing turbines on river or collects water on small storages and flow it to the turbine. This is called Run-of-the-river hydroelectricity. The electricity produced is used to power the nearby localities.</div>
<div>&nbsp;</div>
<div>Advantages over non-renewable sources</div>
<div>&nbsp;</div>
<div>The main advantage of hydroelectric plants is low cost energy. Not like Fossil fuel sources the cost will not vary time by time. Once the plant is installed, the only cost is for maintenance. Since the source is non ending, there is no need of money wastage on source. The plants are generally long living; some plants are in good condition for more than 100 years. Even the construction cost is less comparing nuclear or coal power plants of same production capacity. The labour cost is low too considering the fact that the plans need only minimal attention.</div>
<div>&nbsp;</div>
<div>Hydroelectric dams produce only very minimal amount of CO<sub>2</sub>. That also happens at the time of construction. It is very small comparing the emissions of fossil fuel plants. According to studies, hydroelectricity produces the least amount of green house gases than any other sources. Dams are not only used for energy production but also used for irrigation and agriculture. We can say that hydroelectric dams are the source which creates major social impacts.&nbsp;</div>
<div>&nbsp;</div>
<div>Siltation and flow shortage issues</div>
<div>&nbsp;</div>
<div>The river which brings water to the dam also brings heavier particles with it. This includes organic particles and sand. This will deposit on the dams and gradually reduce its capacity. This is called siltation. This can increase pressure in dams and cause its failure. Since energy production is proportional to the water level, it will&nbsp;seriously&nbsp;affect the power generation. Some plants are abandoned because of excessive siltation in the reservoir. Enloe Dam in America is an example.</div>
<div>&nbsp;</div>
<div>Environmental Issues</div>
<div>&nbsp;</div>
<div>Environmental issues related of dams are mainly due to the area which the dam floods. Since the dams needs large storage area, it has to flood a large region for functioning. Power generation capacity of the dam increases with increase in water storage and it will harm environment proportionally. To date, over 400,000&nbsp;km<sup>2</sup>&nbsp;of the earth have been flooded due to damming.&nbsp;</div>
