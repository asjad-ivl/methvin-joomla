<div>Hydroelectricity - The Solution for Energy Crisis or an outdated technology</div>
<h2>&nbsp;</h2>
<div><br />Introduction&nbsp;&nbsp;&nbsp;&nbsp;</div>
<div>&nbsp;&nbsp;&nbsp;&nbsp;<br />After industrialization, the growth of human race was accelerated. Factories and skyscrapers become a common sight in 20<sup>th</sup>&nbsp;century. Energy became our fundamental need. Our average daily energy usage raised more than 100 times in the past century. The natural energy sources become insufficient for human needs. We start depending on Fossil fuel and nuclear energy which are the most efficient energy sources. Years past, and we are now facing an energy crisis. Fossil fuel is no longer an&nbsp;endless source. Nuclear power plants are causing great damage to environment. It is clear that non-renewable resources are not the way to future. It is the time to look back to the nature.&nbsp;</div>
<br />
<div>&nbsp;</div>
<div>We have endless renewable energy sources in the nature. The most common and traditional energy source is hydroelectricity. We have a long history with hydropower. It starts from Mesopotamia and ancient Egypt since the 6th millennium BC. Water wheels, water mills, turbines, compressed air hydro etc were some popular methods to gain energy from water. There are lot of hydroelectric power generating methods. Dams, Run-of-the-river, pumped-storage etc are some examples. Even though there are many methods, dams and reservoirs are the most common and effective hydroelectric power generation method.&nbsp;</div>
<div>&nbsp;</div>
<div>Hydroelectric Plants</div>
<div>&nbsp;</div>
<div>Hydroelectric plants are one of the first generation technologies. First-generation technologies are most competitive in locations with abundant resources. Among the renewable resources, hydroelectric plants are the oldest power source. Some plants are operating for more than 100 years. The first commercial plant was built near Niagara Falls in US in&nbsp;1881. Then there were a huge growth in number of power plants. By 1889 there were 200 plants in the US alone. At the beginning of 20th century, there was a boom in hydro electricity. Commercial companies start building small power plants in mountains near to cities. By 1920, 40 percentage of US energy was from hydroelectricity. The scenario was almost same in the other developed countries. Relate to damsâ€™ irrigation projects and flood control plans were started.&nbsp;</div>
<div>&nbsp;</div>
<div>In 1930s and 1940s, some massive and effective dams were constructed around the globe. The most famous dam in the world, Hoover Dam was constructed in 1936 with an initial capacity of 1345 MW. Grand Coulee Dam overtook Hoover Dam in 1942 with 6809 MW capacity. There are three dams larger than 10 GW in the world now, the Three Gorges Dam in China with 22.5 GW,&nbsp;Itaipu Dam in Brazil with 14 GW and Guri Dam in Venezuela with 10 GW capacities. Three more are under construction, Xiluodu Dam in China with 12.6 GW, Belo Monte Dam in Brazil with 11.2 GW, Siang Upper HE Project in India with 11 GW.&nbsp;</div>
<div>&nbsp;</div>
<div>The total energy production from hydroelectricity around the world&nbsp; is less than 10%. China is the major producer with 721 terawatt-hours of production per year, accounting 17% their total usage. Currently 150 countries are producing hydroelectricity. Countries like Brazil, Venezuela, Canada, Norway, Austria, New Zealand etc produce majority of their energy from hydropower. Paraguay produces 100% of its electricity from hydroelectric dams, and exports 90% of its production to Brazil and to Argentina. Norway produces 99% energy for their internal energy usage from hydroelectric sources.</div>
