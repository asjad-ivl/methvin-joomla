<div>&nbsp;</div>
<div>In general dams are situated on mountain ranges which are surrounded by thick forest. When a dam is constructed in the area, it floods the whole forest region. In fact a normal dam floods 100 to 500 hectors of surrounding area. Which means a huge number of trees and animals goes under water. It will affect the ecosystem badly. Not only it will kill a large number of species but the anaerobic decay of the forest produce tremendous amount of methane which is a much stronger greenhouse gas than CO<sub>2</sub>. In some cases, the flooded basins are produce methane which has pollution potential 3.5 times more than an oil-fired power plant would for the same generation capacity.</div>
<div>&nbsp;</div>
<div>A contemporary issue related to this is the issue related to the proposed Athirapally Dam.&nbsp; Athirapally is a major tourist place in Trissur district, Kerala. The proposed dam would have submerged more than 140 hectares of forest, dried up extensive riparian areas downstream and destroyed Athirapally Waterfall, one of several scenic waterfalls important to the local tourism industry. The dam also would have destroyed critical habitat of many important species, including the rare Cochin Forest Cane Turtle, the endangered Great Indian Hornbill, the Lion-Tailed Macaque and the Nilgiri Langur. Because of these reasons the dam failed to get the environmental clearance.</div>
<div><br />Another important issue was related to 11 GW Upper Siang Hydroelectric Project. Considering the fact that the project will give a great contribution to the grid, the Environment Ministry gave clearance to the dam. Narmada Bachao Andolan lead by Medha Patkar and Baba Amte against the height increasing of the Sardar Sarovar Dam was an important milestone in protest against environmental issues raised by dams.&nbsp;</div>
<div>&nbsp;</div>
<br />
<div>Social issues</div>
<div>&nbsp;</div>
<br />
<div>Social issues raised by the dams including&nbsp;Diseases&nbsp;and Relocation. Reservoirs can become breeding grounds for disease vectors. This includes mosquitoes and snails which takes advantage of slow moving water. Dams require relocation of large number of population who leaves around the specified area. It is because of the flood plain issue.&nbsp;</div>
<div>&nbsp;</div>
<br />
<div>The record for the largest population relocated belongs to the 22.5 GW Three Gorges Dam built in China. Its reservoir submerged a large area of land, forcing over a million people to relocate. Another example is the Indira Sagar (Polavaram) Project in Andra Pradesh. It is estimated that the project will affect 175,275 persons from Andhra Pradesh, 6,316 persons from Orissa and 11,766 from Chhattisgarh. Polavaram Project would displace about 73,000 Scheduled Tribe persons in 299 villages in the States of Andhra Pradesh, Orissa and Chhattisgarh.&nbsp;</div>
<div>&nbsp;</div>
<br />
<div>Failure threats</div>
<div>&nbsp;</div>
<br />
<div>Dams are considered "installations containing dangerous forces" under International Humanitarian Law due to the massive impact of a possible destruction on the civilian population and the environment. Dam failures are normally rare, but it can wipe out a large population in a very short time.&nbsp;</div>
<div>&nbsp;</div>
<br />
<div>The Banqiao and Shimantan Dam failure (1975) in china is a classic example. It is the biggest of 61 known dam failures. The reason was extreme rainfall beyond the planned design capability of the dam. The dam failures killed an estimated 171,000 people; 11 million people lost their homes. The energy released was equivalent to 18 GW, equivalent to the power of 20 nuclear reactors.&nbsp;</div>
