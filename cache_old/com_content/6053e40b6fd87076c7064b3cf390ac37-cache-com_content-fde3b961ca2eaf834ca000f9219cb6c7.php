The famous&nbsp;Mullaperiyar Dam&nbsp;issue is related to dam failure threat. Mullaperiyar is a 100 year old Dam is situated in Idukki district, Kerala. After the 1979 Morvi Dam failure which killed up to 15,000 people in Gujarat,<sup>&nbsp;</sup>safety concerns of the aging Mullaperiyar dam's were raised by the Kerala Government. The issue become fear to the people of Kerala after the report by IIT Roorkee in 2009 stated that the dam "was likely to face damage if an earthquake of the magnitude of 6.5 on the Richter scale struck its vicinity when the water level is at 136 feet". It is estimated that the failure may affect 3.5 million people in Kerala.&nbsp; Since the water from the dam is extremely important for the farmers of Tamilnadu, the Tamilnadu government is not willing to demolish the dam. The issue is now under the Supreme Court.</div>
<div>&nbsp;</div>
<br />
<div>Conclusion</div>
<div>&nbsp;</div>
<br />
<div>Hydroelectricity is an effective and important form of renewable energy. It is a first generation sustainable energy technology.&nbsp;Generally speaking, hydroelectric plants produce much lower life-cycle emissions than other types of power generation. Some plants are still working for more than 100 years. But the dams constructed cause various environmental and social issues. To avoid those there should be better planning and environmental studies before every project. It is better to concentrate on second and third generation technologies like solar energy, biomass and ocean energy which cause minimal impact to earth.</div>
