<h4><strong>Sheet Piling On reclamation, sand fill, sheet piling by crane and drop hammer, vibro hammer or diesel hammer.</strong></h4>
<ul>
<li>A rate of 20 - 25 No/day to 20 to 35 m deep.</li>
<li>1 no = 0.4 m wide on plan and 6 m long.</li>
<li>Therefore 8 - 10 m/day per rig is achievable.</li>
</ul>
<p>&nbsp;</p>
<h4><strong>On more difficult areas, such as through existing ground or a sea wall for example.</strong></h4>
<ul>
<li>A rate of 10 - 12 No/day to 20 m deep.</li>
<li>Therefore 4 - 6 m/day per rig is achievable.</li>
</ul>
<p>For more shallow sheet pile walls</p>
<ul>
<li>Driven by - Pilemaster 60.0 m/day</li>
<li>Driven by - Automatic Drop Hammer 32.0 m/day</li>
</ul>
<p>Steel for piles is usually readily available and a two week lead in is often all that is required.</p>
