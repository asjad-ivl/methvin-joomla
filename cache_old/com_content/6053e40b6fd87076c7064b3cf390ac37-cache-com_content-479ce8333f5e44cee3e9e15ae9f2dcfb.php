		<h2>Project Proverbs</h2>
<p>&nbsp;</p>
<ul>
<li>The same work under the same conditions will be estimated differently by ten different estimators, or by one estimator at ten different times</li>
<li>The most valuable and least used word in a project manager's vocabulary is "NO"</li>
<li>You can con a sucker into committing to an unreasonable deadline, but you can't bully him into meeting it</li>
<li>The more ridiculous the deadline, the more it costs to meet it</li>
<li>The more desperate the situation, the more optimistic the situatee</li>
<li>Too few people on a project can't solve the problem - too many create more problems than they solve</li>
<li>You can freeze the clients specifications, but he won't stop expecting</li>
<li>Frozen specifications and the abominable snowman are alike :- they both are myths, and they both melt when sufficient heat is applied</li>
<li>The conditions attached to a promise are forgotten, and the promise is remembered</li>
<li>What you don't know hurts you</li>
<li>A client will tell you anything you ask - but nothing more</li>
<li>Of several possible interpretations of a communication, the least convenient one is the only correct one</li>
<li>What is not on paper has not been said</li>
<li>No major project is ever installed on time, within budget, and with the same staff that started it</li>
<li>Projects progress quickly until they become 95% complete; then they remain at 95% complete forever</li>
<li>If project content is allowed to change freely, then the rate of change will soon exceed the rate of progress</li>
<li>No major system is ever completely debugged; attempts to debug a system inevitably introduce new bugs that are even harder to find</li>
<li>Project teams detest progress reporting because it vividly demonstrates their lack of progress</li>
</ul>
<ul>
<li>There's never enough time to do it right first time but there's always enough time to go back and do it again.</li>
<li>If you fail to plan you are planning to fail.</li>
<li>A little risk management saves a lot of fan cleaning.</li>
<li>The sooner you get behind schedule, the more time you have to make it up.</li>
<li>A badly planned project will take three times longer than expected - a well planned project only twice as long as expected.</li>
<li>When all's said and done a lot more is said than done.</li>
<li>If at first you don't succeed, remove all evidence you ever tried.</li>
<li>The more you plan the luckier you get.</li>
<li>Good project management is not so much knowing what to do and when, as knowing what excuses to give and when.</li>
<li>Everyone asks for a strong project manager - when they get him they don't want him.</li>
<li>Overtime is a figment of the naïve project manager's imagination.</li>
<li>All project managers face problems on Monday mornings - good project managers are working on next Monday's problems.</li>
<li>Never underestimate the ability of senior management to buy a bad idea and fail to buy a good idea.</li>
<li>If it wasn't for the 'last minute' nothing would get done.</li>
<li>Meetings are where minutes are kept and hours are lost.</li>
<li>Good project managers don't solve problems, they get rid of them.</li>
<li>If you want to make Murphy laugh: have a definite plan.</li>
<li>If everything seems to be going well, you obviously don't know what's going on (Edward Murphy).</li>
<li>The plan is nothing. Planning is everything (President Dwight D. Eisenhower)</li>
<li>"The difference between genius and stupidity is that genius has its&nbsp;limits." - Albert Einstein</li>
<li>"Two things are infinite: the universe and human stupidity; and I'm not&nbsp;sure about the universe." - Albert Einstein</li>
</ul>
<p>&nbsp;</p>
<h2>Project Management Laws</h2>
<p>&nbsp;</p>
<ul>
<li>If it can go wrong it will - Murphy's law.</li>
<li>If it can't possibly go wrong, it will - O'Malley's corollary to Murphy's law.</li>
<li>It will go wrong in the worst possible way - Sod's law.</li>
<li>Work expands to fill the time available for its completion - Parkinson's law.</li>
<li>Finely chopped cabbage in mayonnaise - Coleslaw.</li>
<li>If there is a 50% chance of something going wrong then 9 times out of 10 it will.</li>
<li>A two year project will take three years, a three year project will never finish - (anyone know who's law this is?)</li>
<li>Murphy, O'Malley, Sod and Parkinson are alive and well - and working on your project</li>
<li>Matzen's law. “For every function, there is an equal and opposite malfunction.”</li>
</ul>
